"""SimLens performance harness (T1.3).

Measures per-`explain` latency at dim 384/768/1536, batch throughput, and per-query SAE
encode cost, plus peak memory. Emits a Markdown table (``--md``) or JSON (``--json``).

    python benchmarks/bench.py --md > docs/benchmarks.md
"""
from __future__ import annotations

import argparse
import json
import time
import tracemalloc

import numpy as np

import simlens
from simlens import train
from simlens.train import build_bundle


def _time(fn, iters: int, warmup: int = 5) -> float:
    for _ in range(warmup):
        fn()
    t0 = time.perf_counter()
    for _ in range(iters):
        fn()
    return (time.perf_counter() - t0) / iters * 1e6  # microseconds/call


def _corpus(dim: int, n: int, seed: int = 0):
    rng = np.random.default_rng(seed)
    kc = 8
    protos = rng.standard_normal((kc, dim))
    protos /= np.linalg.norm(protos, axis=1, keepdims=True)
    mem = rng.random((n, kc)) < 0.25
    X = (mem @ protos + 0.05 * rng.standard_normal((n, dim))).astype(np.float32)
    return X


def run(dims=(384, 768, 1536), n=2000, iters=2000) -> list[dict]:
    rows = []
    for dim in dims:
        X = _corpus(dim, n)
        q, c = X[0], X[1]

        ex_dim = simlens.Explainer(metric="cosine")
        dim_us = _time(lambda: ex_dim.explain(q, c, level="dim"), iters)

        # a trained bundle for feature-level timing
        sae = train.fit(X, arch="topk", k=32, expansion=8, epochs=8)
        bundle = build_bundle("bench", "cosine", sae, X)
        ex_feat = simlens.Explainer(bundle)
        feat_us = _time(lambda: ex_feat.explain(q, c, level="feature"), max(iters // 4, 200))

        nat = bundle.to_native_sae()
        enc_us = _time(lambda: nat.encode_np(np.ascontiguousarray(q)), iters)

        # batch throughput (zero-copy batch encode)
        Xb = np.ascontiguousarray(X[:1000])
        t0 = time.perf_counter()
        nat.encode_batch_np(Xb)
        batch_s = time.perf_counter() - t0
        throughput = len(Xb) / batch_s

        tracemalloc.start()
        _ = ex_feat.explain(q, c, level="feature")
        peak_kb = tracemalloc.get_traced_memory()[1] / 1024
        tracemalloc.stop()

        rows.append({
            "dim": dim,
            "n_features": sae.n_features,
            "explain_dim_us": round(dim_us, 2),
            "explain_feature_us": round(feat_us, 2),
            "sae_encode_us": round(enc_us, 2),
            "batch_encode_vecs_per_s": int(throughput),
            "explain_peak_kb": round(peak_kb, 1),
        })
    return rows


def to_md(rows: list[dict]) -> str:
    out = [
        "# SimLens benchmarks",
        "",
        "Generated by `python benchmarks/bench.py --md`. Times are per-call medians on the "
        "host CPU; treat them as relative, not absolute. `explain_dim` is the exact Level-1 "
        "decomposition (no bundle); `explain_feature` is Level-2 over a TopK SAE "
        "(expansion 8, k=32); `sae_encode` and `batch_encode` use the zero-copy numpy FFI.",
        "",
        "| dim | features | explain dim (µs) | explain feature (µs) | SAE encode (µs) | "
        "batch encode (vec/s) | explain peak (KB) |",
        "|----:|---------:|-----------------:|---------------------:|----------------:|"
        "---------------------:|------------------:|",
    ]
    for r in rows:
        out.append(
            f"| {r['dim']} | {r['n_features']} | {r['explain_dim_us']} | "
            f"{r['explain_feature_us']} | {r['sae_encode_us']} | "
            f"{r['batch_encode_vecs_per_s']:,} | {r['explain_peak_kb']} |"
        )
    out += ["", "_Run `cargo bench -p simlens-core` for the Rust-kernel micro-benchmarks._", ""]
    return "\n".join(out)


def main(argv=None):
    p = argparse.ArgumentParser()
    p.add_argument("--md", action="store_true", help="emit a Markdown table")
    p.add_argument("--json", action="store_true", help="emit JSON")
    p.add_argument("--iters", type=int, default=2000)
    p.add_argument("--dims", type=int, nargs="+", default=[384, 768, 1536])
    args = p.parse_args(argv)
    rows = run(dims=tuple(args.dims), iters=args.iters)
    if args.json:
        print(json.dumps(rows, indent=2))
    else:
        print(to_md(rows))


if __name__ == "__main__":
    main()
